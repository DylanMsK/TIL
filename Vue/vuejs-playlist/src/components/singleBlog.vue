<template>
  <div id="single-blog">
    <h1>{{ blog.title }}</h1>
    <article>{{ blog.content }}</article>
    <p>Author {{ blog.author }}</p>
    <ul>
      <li v-for="category in blog.categories">{{ category }}</li>
    </ul>
  </div>
</template>

<script>
export default {
  data() {
    return {
      id: this.$route.params.id,
      blog: {}
    };
	},
	created() {
		this.$http.get(`https://vuejs-playlist-d4529.firebaseio.com/psots/${this.id}.json`).then(data => {
      return data.json();
		}).then(data => {
      this.blog = data;
    })
	}
};
</script>

